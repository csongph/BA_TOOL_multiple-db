CREATE TABLE hrEmployee_Profile (
	EmployeeID varchar(36) COLLATE Thai_CI_AS NOT NULL,
	EmployeeCode nvarchar(50) COLLATE Thai_CI_AS NOT NULL,
	FirstName nvarchar(150) COLLATE Thai_CI_AS NOT NULL,
	LastName nvarchar(150) COLLATE Thai_CI_AS NOT NULL,
	FirstNameEng nvarchar(150) COLLATE Thai_CI_AS NULL,
	LastNameEng nvarchar(150) COLLATE Thai_CI_AS NULL,
	Gender nvarchar(10) COLLATE Thai_CI_AS NULL,
	BirthDate date NULL,
	HireDate datetime NULL,
	Email nvarchar(255) COLLATE Thai_CI_AS NULL,
	PhoneNumber varchar(20) COLLATE Thai_CI_AS NULL,
	Salary decimal(12,2) NULL,
	Bonus float NULL,
	IsActive bit NOT NULL,
	IsResigned bit NULL,
	DepartmentID varchar(36) COLLATE Thai_CI_AS NULL,
	PositionID varchar(36) COLLATE Thai_CI_AS NULL,
	CreatedBy varchar(36) COLLATE Thai_CI_AS NULL,
	CreatedDate datetime NULL,
	ModifiedBy varchar(36) COLLATE Thai_CI_AS NULL,
	ModifiedDate datetime NULL,
	Remark nvarchar(500) COLLATE Thai_CI_AS NULL
);